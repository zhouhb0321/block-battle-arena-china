// Auto-detect language based on browser/location
const detectLanguage = (): Language => {
  const browserLang = navigator.language || navigator.languages?.[0] || 'en';
  const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone;
  
  if (browserLang.startsWith('zh')) {
    if (browserLang.includes('TW') || browserLang.includes('HK') || timezone.includes('Taipei')) {
      return 'zh-TW';
    }
    return 'zh';
  } else if (browserLang.startsWith('ja') || timezone.includes('Tokyo')) {
    return 'ja';
  } else if (browserLang.startsWith('ko') || timezone.includes('Seoul')) {
    return 'ko';
  } else if (browserLang.startsWith('es')) {
    return 'es';
  }
  
  return 'en';
};

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>('en');

  useEffect(() => {
    const savedLanguage = localStorage.getItem('language') as Language;
    if (savedLanguage && translations[savedLanguage]) {
      setLanguage(savedLanguage);
    } else {
      setLanguage('en');
      localStorage.setItem('language', 'en');
    }
  }, []);

  const handleSetLanguage = (lang: Language) => {
    setLanguage(lang);
    localStorage.setItem('language', lang);
  };

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage: handleSetLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
